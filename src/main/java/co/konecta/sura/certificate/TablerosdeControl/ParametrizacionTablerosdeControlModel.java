package co.konecta.sura.certificate.TablerosdeControl;

public class ParametrizacionTablerosdeControlModel {
    private String camposMostrar;
    private String roles;
    private String asesores;
    private String soloMisExpedientes;



    public String getCamposMostrar() {
        return camposMostrar;
    }

    public void setCamposMostrar(String camposMostrar) {
        this.camposMostrar = camposMostrar;
    }

    public String getRoles() {
        return roles;
    }

    public void setRoles(String roles) {
        this.roles = roles;
    }

    public String getAsesores() {
        return asesores;
    }

    public void setAsesores(String asesores) {
        this.asesores = asesores;
    }

    public String getSoloMisExpedientes() {
        return soloMisExpedientes;
    }

    public void setSoloMisExpedientes(String soloMisExpedientes) {
        this.soloMisExpedientes = soloMisExpedientes;
    }
}

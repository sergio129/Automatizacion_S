package co.konecta.sura.certificate.Modelos.AdministrarTareas;

public class TareasMonitoreoModel {
    private String nombre;
    private String caso;
    private String estadoTareas;
    private String linea;
    private String tipoMonitoreo;
    private String gestionCoordinacion;
    private String fechaInicio;
    private String fechaFin;
    private String usuarioAsignado;
    private String tareasVencidas;
    private String usuario;
    private String estadoTarea;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCaso() {
        return caso;
    }

    public void setCaso(String caso) {
        this.caso = caso;
    }

    public String getEstadoTareas() {
        return estadoTareas;
    }

    public void setEstadoTareas(String estadoTareas) {
        this.estadoTareas = estadoTareas;
    }

    public String getLinea() {
        return linea;
    }

    public void setLinea(String linea) {
        this.linea = linea;
    }

    public String getTipoMonitoreo() {
        return tipoMonitoreo;
    }

    public void setTipoMonitoreo(String tipoMonitoreo) {
        this.tipoMonitoreo = tipoMonitoreo;
    }

    public String getGestionCoordinacion() {
        return gestionCoordinacion;
    }

    public void setGestionCoordinacion(String gestionCoordinacion) {
        this.gestionCoordinacion = gestionCoordinacion;
    }

    public String getFechaInicio() {
        return fechaInicio;
    }

    public void setFechaInicio(String fechaInicio) {
        this.fechaInicio = fechaInicio;
    }

    public String getFechaFin() {
        return fechaFin;
    }

    public void setFechaFin(String fechaFin) {
        this.fechaFin = fechaFin;
    }

    public String getUsuarioAsignado() {
        return usuarioAsignado;
    }

    public void setUsuarioAsignado(String usuarioAsignado) {
        this.usuarioAsignado = usuarioAsignado;
    }

    public String getTareasVencidas() {
        return tareasVencidas;
    }

    public void setTareasVencidas(String tareasVencidas) {
        this.tareasVencidas = tareasVencidas;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getEstadoTarea() {
        return estadoTarea;
    }

    public void setEstadoTarea(String estadoTarea) {
        this.estadoTarea = estadoTarea;
    }
}
